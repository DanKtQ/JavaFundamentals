package theory.oop.session17.valueVsReferenceType;

public class PrimitiveDataType {

    // -128 pana la 127
    byte myByte = 12;

    // 16 bit integer value
    // -32768 pana la 32767
    short myShort = 1234;

    // 32 bit integer value
    int myInt = 7897;

    //64 bit integer value
    long myLong = 123456;

    //
    double myDouble = 12.55;

    // 64 bit integer value
    // about 15 decimal digits
    float myFloat = 6.3f;

    boolean myBoolean = false;
    boolean testBoolean=(myInt>200);

    // un singur caracter
    char myChar = 'w';
}
